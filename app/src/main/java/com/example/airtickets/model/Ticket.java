package com.example.airtickets.model;

public class Ticket {
    private String from;
    private String dest;
    private int cost;
    private String date;

    public String getFrom() {
        return from;
    }

    public String getDest() {
        return dest;
    }

    public int getCost() {
        return cost;
    }

    public String getDate() {
        return date;
    }
}
